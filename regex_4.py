import sys; args = sys.argv[1:]
idx = int(args[0])-60
myRegexLst = [
  r"/^((?!010)[01])*$/", # 60
  r"/^((?!010|101)[01])*$/", # 61
  r"/^(0|1)([01]*\1)*$/", # 62
  r"/\b((\w)(?!\w*\2\b))+\b/i",# 63
  r"/((\w)*(\w*\2){3}|\w*(?=(\w)\w*\4)\w+(?!\4)(\w)\w*\5)\w*/i", # 64
  r"/\b(?=(\w)*(\w*\1){2})(\1|(\w)(?!\w*\4))+\b/i", # 65
  r"/\b(?!\w*([aeiou])\w*\1)(\w*[aeiou]){5}[^aeiou ]*\b/",# 66
  r"/^(?=1*0(1|01*0)*$)(0|10*1)*$/",# 67
  r"/^0$|^((1(01*0)*1)+0*)+$/",# 68
  r"/^(?!(1(01*0)*1|0)+$)[10]+$/"# 69
]
if idx < len(myRegexLst):
  print(myRegexLst[idx])

